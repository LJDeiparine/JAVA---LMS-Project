package FourPillars;

class Records {
	private int studentsHandled = 0;
	private int students = 0;
	public void setStudentsHandled (int count)
	   {
		studentsHandled = count;
	   }
	   public double getStudentsHandled () 
	   {
	       return studentsHandled;
	   }
	   
	   public void setStudents (int count)
	   {
		   students = count;
	   }
	   public int getStudents () 
	   {
	       return students;
	   }
}
